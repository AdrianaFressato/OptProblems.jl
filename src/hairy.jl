function hairy()
  h = 30 #hlength
  cs = 100 #cslope
  x = [-5;-7]
  f(x) = (sin(7*x[1])^2)*(cos(7*x[2])^2*h) + cs*sqrt(0.01+(x[1] - x[2])^2) + cs*sqrt(0.01+x[1]^2)
  g(x) = [7*h*sin(14*x[1])*(cos(7*x[2])^2) + (cs*(x[1]-x[2])/sqrt((x[1] - x[2])^2 + 0.01)) + (cs*x[1]/sqrt(x[1]^2 + 0.01));
         7*h*(sin(7*x[1])^2)*sin(14*x[2]) + (cs*(x[1]-x[2])/sqrt((x[1] - x[2])^2 + 0.01))]
  H(x) = [98*h*cos(14*x[1])*(cos(7*x[2])^2) + cs*0.01/(sqrt((x[1] - x[2])^2 + 0.01)*(x[1]^2 - 2*x[1]*x[2] + x[2]^2 + 0.01))
   -49*h*sin(14*x[1])*sin(14*x[2]) - cs*0.01/(sqrt((x[1] - x[2])^2 + 0.01)*(x[1]^2 - 2*x[1]*x[2] + x[2]^2 + 0.01));
   -49*h*sin(14*x[1])*sin(14*x[2]) - cs*0.01/(sqrt((x[1] - x[2])^2 + 0.01)*(x[1]^2 - 2*x[1]*x[2] + x[2]^2 + 0.01))
   -98*h*(sin(7*x[1])^2)*cos(14*x[2]) + c*0.01/(sqrt((x[1] - x[2])^2 + 0.01)*(x[1]^2 - 2*x[1]*x[2] + x[2]^2 + 0.01))]
  return f, g, H, x
end
